package com.example.team_2_tdp_mt;

public class ReportValue {

    long xValue;
    int yValue;

    public ReportValue() {
    }

    public ReportValue(long xValue, int yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public long getxValue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }

    public void setxValue(long xValue) {
        this.xValue = xValue;
    }

    public void setyValue(int yValue) {
        this.yValue = yValue;
    }
}

